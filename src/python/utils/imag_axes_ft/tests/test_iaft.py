def ir_filename(lmbda: float, prec: float):
    if lmbda == 100:
        lmbda_str = "1e2"
    elif lmbda == 1000:
        lmbda_str = "1e3"
    elif lmbda == 10000:
        lmbda_str = "1e4"
    elif lmbda == 100000:
        lmbda_str = "1e5"
    elif lmbda == 1000000:
        lmbda_str = "1e6"
    else:
        raise ValueError("Unsupported lambda value")

    if prec == 1e-6:
        prec_str = "1e-06"
    elif prec == 1e-10:
        prec_str = "1e-10"
    elif prec == 1e-15:
        prec_str = "1e-15"
    else:
        raise ValueError("Unsupported precision value")

    filename = coqui.INSTALL_DIR+"/data/ir/"+lmbda_str+"."+prec_str+".h5"
    if os.path.isfile(filename):
        return filename

    filename = coqui.PROJECT_SOURCE_DIR+"/src/numerics/imag_axes_ft/ir/data/ir/" + lmbda_str + "." + prec_str + ".h5"
    if os.path.isfile(filename):
        return filename

    raise FileNotFoundError(f"IR data file not found for lambda={lmbda}, prec={prec}")


def compare_sparse_ir_h5(wmax: float, prec: str):
    """
    Compare the IR basis generated by sparse_ir with the precomputed basis stored in the HDF5 file.
    This check ensures compatibility of sparse_ir across different library versions.

    :param wmax: frequency cutoff
    :param prec: "low" (1e-6), "medium" (1e-10), "high" (1e-15)
    """
    ir = IAFT(beta=1.0, wmax=wmax, prec=prec)
    with HDFArchive(ir_filename(ir.lmbda, ir.prec), "r") as ar:
        tau_mesh = ar["fermion"]["tau_mesh"]
        wn_mesh = ar["fermion"]["wn_mesh"]
        assert np.allclose(tau_mesh, ir.tau_mesh(stats="f"), atol=1e-12)
        assert np.allclose(wn_mesh, ir.wn_mesh(stats='f'), atol=1e-12)

        tau_mesh = ar["boson"]["tau_mesh"]
        wn_mesh = ar["boson"]["wn_mesh"]
        assert np.allclose(tau_mesh, ir.tau_mesh(stats="b"), atol=1e-12)
        assert np.allclose(wn_mesh, ir.wn_mesh(stats='b'), atol=1e-12)

        Ttw = ar["fermion"]["Ttw"] * np.sqrt(2)
        Twt = ar["fermion"]["Twt"] / np.sqrt(2)
        assert np.allclose(Ttw, ir.Ttw_ff, atol=1e-12)
        assert np.allclose(Twt, ir.Twt_ff, atol=1e-12)

        Ttw = ar["boson"]["Ttw"] * np.sqrt(2)
        Twt = ar["boson"]["Twt"] / np.sqrt(2)
        assert np.allclose(Ttw, ir.Ttw_bb, atol=1e-12)
        assert np.allclose(Twt, ir.Twt_bb, atol=1e-12)



def test_ir_compatibility():
    wmax_list = [90.0, 900.0, 9000.0, 90000.0]
    for wmax in wmax_list:
        compare_sparse_ir_h5(wmax=wmax, prec="low")
        compare_sparse_ir_h5(wmax=wmax, prec="medium")
        compare_sparse_ir_h5(wmax=wmax, prec="high")


@pytest.mark.parametrize("stats,ph_sym,basis", [
    ('f', False, "dlr"),
    ('b', False, "dlr"),
    ('b', True, "dlr"),
    ('f', False, "ir"),
    ('b', False, "ir"),
    ('b', True, "ir"),
])
def test_fourier_transform(stats, ph_sym, basis):
    """G(tau) and G(iw) returned by the two builders should be consistent via IAFT."""
    norb = 2
    beta = 5.0
    iaft = IAFT(beta=beta, wmax=100.0, prec="high", basis=basis)

    G_tau_ref = iaft.build_g_tau_ref(norb, stats, ph_sym)  # shape (nt_f, norb, norb)
    G_iw_ref = iaft.build_g_iw_ref(norb, stats, ph_sym)  # shape (nw_f, norb, norb)

    G_iw_computed = iaft.tau_to_w(G_tau_ref, stats=stats, ph_sym=ph_sym)
    G_tau_computed = iaft.w_to_tau(G_iw_ref, stats=stats, ph_sym=ph_sym)

    assert np.allclose(G_iw_computed, G_iw_ref, atol=1e-8), (
        f"Max deviation: {np.max(np.abs(G_iw_computed - G_iw_ref))}"
    )
    assert np.allclose(G_tau_computed, G_tau_ref, atol=1e-8), (
        f"Max deviation: {np.max(np.abs(G_tau_computed - G_tau_ref))}"
    )

    # tau interpolation
    iaft_interp = IAFT(beta=beta, wmax=10.0, prec="high", basis=basis)
    G_tau_interp_ref = iaft_interp.build_g_tau_ref(norb, stats, ph_sym)  # shape (nt_f, norb, norb)
    G_tau_interp = iaft.tau_interpolate(G_tau_ref, iaft_interp, stats, ph_sym)

    assert np.allclose(G_tau_interp, G_tau_interp_ref, atol=1e-8), (
        f"Max deviation: {np.max(np.abs(G_tau_interp - G_tau_interp_ref))}"
    )

    # iw interpolation
    G_iw_interp_ref = iaft_interp.build_g_iw_ref(norb, stats, ph_sym)  # shape (nw_interp, norb, norb)
    G_iw_interp = iaft.w_interpolate(G_iw_ref, iaft_interp, stats, ph_sym=ph_sym)

    assert np.allclose(G_iw_interp, G_iw_interp_ref, atol=1e-8), (
        f"Max deviation: {np.max(np.abs(G_iw_interp - G_iw_interp_ref))}"
    )

    # Test the old API for ph-symmetric case
    if ph_sym:
        G_iw_computed = iaft.tau_to_w_phsym(G_tau_ref, stats=stats)
        G_tau_computed = iaft.w_to_tau_phsym(G_iw_ref, stats=stats)
        assert np.allclose(G_iw_computed, G_iw_ref, atol=1e-8), (
            f"Max deviation: {np.max(np.abs(G_iw_computed - G_iw_ref))}"
        )
        assert np.allclose(G_tau_computed, G_tau_ref, atol=1e-8), (
            f"Max deviation: {np.max(np.abs(G_tau_computed - G_tau_ref))}"
        )

        G_tau_interp = iaft.tau_interpolate_phsym(G_tau_ref, iaft_interp, stats)
        assert np.allclose(G_tau_interp, G_tau_interp_ref, atol=1e-8), (
            f"Max deviation: {np.max(np.abs(G_tau_interp - G_tau_interp_ref))}"
        ) 
        G_iw_interp = iaft.w_interpolate_phsym(G_iw_ref, iaft_interp, stats)
        assert np.allclose(G_iw_interp, G_iw_interp_ref, atol=1e-8), (
            f"Max deviation: {np.max(np.abs(G_iw_interp - G_iw_interp_ref))}"
        )


def test_iaft_ir_rejects_eps():
    with pytest.raises(ValueError, match="IR basis accepts only 'prec'"):
        IAFT(beta=5.0, wmax=10.0, eps=1e-10, basis="ir")


def test_iaft_dlr_requires_at_least_one_accuracy_arg():
    with pytest.raises(ValueError, match="at least one of 'prec' or 'eps'"):
        IAFT(beta=5.0, wmax=10.0, prec=None, eps=None, basis="dlr")


def test_iaft_dlr_prefers_prec_when_both_and_prec_not_custom():
    iaft = IAFT(beta=5.0, wmax=10.0, prec="high", eps=1e-11, basis="dlr", verbose=False)
    assert iaft.prec == "high"
    assert iaft.eps == pytest.approx(1e-13)


# meaning that "custom" is redundant when eps is provided. 
def test_iaft_dlr_prefers_eps_when_both_and_prec_custom():
    iaft = IAFT(beta=5.0, wmax=10.0, prec="custom", eps=1e-11, basis="dlr", verbose=False)
    assert iaft.prec == "custom"
    assert iaft.eps == pytest.approx(1e-11)


def test_iaft_dlr_accepts_eps_and_roundtrips_checkpoint():
    iaft = IAFT(beta=5.0, wmax=10.0, prec=None, eps=1e-11, basis="dlr", verbose=False)
    assert iaft.prec == "custom"
    assert iaft.eps == pytest.approx(1e-11)

    with tempfile.TemporaryDirectory() as tmpdir:
        chkpt = os.path.join(tmpdir, "iaft_test.h5")
        with HDFArchive(chkpt, 'w') as ar:
            iaft.save(ar)

        with HDFArchive(chkpt, 'r') as ar:
            iaft_grp = ar['imaginary_fourier_transform']
            assert iaft_grp['basis'] == 'dlr'

        restored = IAFT.from_coqui_chkpt(chkpt, verbose=False)
        assert restored == iaft
        assert restored.eps == pytest.approx(1e-11)
        assert restored.prec == "custom"


def test_iaft_restores_from_legacy_source_checkpoint():
    with tempfile.TemporaryDirectory() as tmpdir:
        chkpt = os.path.join(tmpdir, "iaft_legacy_source.h5")
        with HDFArchive(chkpt, 'w') as ar:
            ar.create_group('imaginary_fourier_transform')
            iaft_grp = ar['imaginary_fourier_transform']
            iaft_grp['beta'] = 5.0
            iaft_grp['wmax'] = 10.0
            iaft_grp['prec'] = 'high'
            iaft_grp['source'] = 'ir'

        restored = IAFT.from_coqui_chkpt(chkpt, verbose=False)
        assert restored.beta == pytest.approx(5.0)
        assert restored.wmax == pytest.approx(10.0)
        assert restored.prec == 'high'
        assert restored.basis == 'ir'
